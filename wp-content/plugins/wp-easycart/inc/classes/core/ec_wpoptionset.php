			$item = new ec_wpoption($this->wp_option_names[$i], $this->wp_option_defaults[$i]);
			array_push($this->wp_options, $item);	
		}
		
	}
	
	public function add_options(){
		
		for($i=0; $i<count($this->wp_option_names); $i++){
			add_option($this->wp_options[$i]->wp_option_name, $this->wp_options[$i]->wp_option_default);
		}
		
	}
	
	public function update_options(){
		
		for($i=0; $i<count($this->wp_option_names); $i++){
			add_option($this->wp_options[$i]->wp_option_name, $this->wp_options[$i]->wp_option_default);
		}
		
	}
	
	public function register_options(){
		for($i=0; $i<count($this->wp_options); $i++){
			if( isset( $this->wp_option_groups[$i] ) ){
				register_setting( $this->wp_option_groups[$i], $this->wp_options[$i]->wp_option_name);
			}
		}
	}
	
	public function delete_options(){
		
		for($i=0; $i<count($this->wp_option_names); $i++){
			delete_option($this->wp_options[$i]->wp_option_name);
		}
		delete_option( 'ec_option_db_new_version' );
		delete_option( 'ec_option_db_insert_v4' );
		delete_option( 'ec_option_published_check' );
		delete_option( 'ec_option_v3_fix' );
		delete_option( 'ec_option_wpoptions_version' );
		delete_option( 'ec_option_data_folders_installed' );
		
	}
	
	public function get_db_dump_options( ){
		
		global $wpdb;
		$return_string = "";
		for($i=0; $i<count($this->wp_option_names); $i++){
			$return_string .= "UPDATE " . $wpdb->prefix . "options SET option_value = '" . str_replace( "'", "''", stripslashes( get_option( $this->wp_options[$i]->wp_option_name ) ) ) . "' WHERE option_name = '" . $this->wp_options[$i]->wp_option_name . "';\n";
		}
		return $return_string;
		
	}
	
